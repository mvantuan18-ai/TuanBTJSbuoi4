let ngay = Number(prompt("Nhập số (1-7) đại diện cho ngày trong tuần:"));
switch (ngay) {
  case 1:
   console.log("Thứ hai");
    break;
  case 2:
    console.log("Thứ ba");
    break;
  case 3:
    console.log("Thứ tư");
    break;
  case 4:
    console.log("Thứ năm");
    break;
  case 5:
    console.log("Thứ sáu");
    break;
  case 6:
    console.log("Nghỉ ngơi");
    break;
  case 7:
    console.log("Nghỉ ngơi");
    break;
  default:
    console.log("Giá trị không hợp lệ");
    break;
}
